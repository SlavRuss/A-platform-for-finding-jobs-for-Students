def search_regions(keyword):
    print(f"\n=== Результаты поиска регионов по '{keyword}' ===")
    results = []
    for region_id, region_name in REGIONS.items():
        if keyword.lower() in region_name.lower():
            results.append((region_id, region_name))
    
    if results:
        for region_id, region_name in results:
            print(f"{region_id:3d} - {region_name}")
        print(f"\nНайдено регионов: {len(results)}")
    else:
        print("Регионы не найдены")
    
    return results

